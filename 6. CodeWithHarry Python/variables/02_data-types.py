# Python supports several build-in data types:
#       Integers (int): Whole numbers (e.g., 10 , -5),
#       Float (float): Decimal numbers (e.g., 3.14 , -0.001)
#       Strings (str): Text data exclosed in quotes (e.g., "Anand" , "Cool")
#       Booleans (bool): Represents True or False
#       Lists: Ordered, mutuable collections (e.g., [1,2,3])
#       Tuples: Ordered, mutuable collections (eg., (1,2,3))
#       Sets: Unrdered collections of unique elements (e.g., {1 ,2 ,3})
#       Dictionaries: Key-value pairs (e.g., {"name": "Alice", "age": 25})

age = 3
print(age)
print(type(age))

cgpa = 8.2
print(cgpa)
print(type(cgpa))

name="anand"
print(name)
print(type(name))

is_complted = True  # can be also be False
print(is_complted)
