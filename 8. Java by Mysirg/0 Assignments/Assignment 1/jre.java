// JRE
// -> JRE is Java Runtime Environment.
// -> It is a software layer that runs on top of a computer's operating system.
// -> It provides the class libraries and other resources that a specific Java Program needs to run.
// -> JRE creates an instance of JVM to run that executor the Java Program.
// -> JRE jas class loader to dynamically loads all the classes necessary to run a Java Program.

